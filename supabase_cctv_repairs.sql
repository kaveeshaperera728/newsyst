-- Create CCTV Repairs Table
create table cctv_repairs (
  id bigint generated by default as identity primary key,
  "cctvId" bigint references cctv(id) on delete cascade,
  "faultDescription" text not null,
  "actionTaken" text, -- e.g., Replaced with new unit, Repaired wiring
  cost numeric,
  date date not null,
  technician text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);
